
package examenJavaEjerciciosInternett;

public class Ejercicio11NumImpar {
  public void multi (int a){
      int multi=1;
      
      
      
      
      
      for(int i=1;i<a;i+=2){
          
          multi=multi*i;
          
          if(multi>60000000){
          System.out.println(multi);
          }
      }
      
      
      
      
      
      
      
      
      
      
  }
    
    
    
    
    
    
    
    
    
    
    
    
}

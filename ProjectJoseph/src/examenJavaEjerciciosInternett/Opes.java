package examenJavaEjerciciosInternett;

public class Opes {

    public double Suma(int a, int b) {
        double result = a + b;
        return result;
    }

    public double Resta(int a, int b) {
        double result = a - b;
        return result;
    }

    public double Multi(int a, int b) {
        double result = a * b;
        return result;
    }

    public double Divi(int a, int b) {
        double result = a / b;
        return result;
    }

}

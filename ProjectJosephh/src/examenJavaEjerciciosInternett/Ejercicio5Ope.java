package examenJavaEjerciciosInternett;

public class Ejercicio5Ope {

    public double cali(int p, int e, int ex, int f) {

        double total1 = (p * 10) / 100;
        double total2 = (e * 25) / 100;
        double total3 = (ex * 25) / 100;
        double total4 = (f * 40) / 100;
        double total = total1 + total2 + total3 + total4;
        return total;

    }

}


package examenJavaEjerciciosInternett;

public class Ejercicio7Ope {
    public int horas(int h){
     int sem= h/(7*24);    
     int dia= (h-sem*7*24)/24;   
     int hora= (1000-((sem*7*24)+(dia*24)));
       
     
     return hora;
        
        
        
        
        
        
        
        
        
        
        
        
        
    }
    
}
